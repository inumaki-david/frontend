// Funções Matemáticas (Math) -> Nativas

    //Função de Raiz Quadrada (Math.sqrt)
    console.log(Math.sqrt(25)); //5

    //Função de Potência de um n° por Outro (Math.pow)
    console.log(Math.pow(7,2)); //7²=49
    console.log(Math.pow(4,3)); //4³=64
    console.log(Math.pow(27,1/3)); //³√27=3

    // Funções de Arredondamento (Math.round, Math.ceil, Math.floor)
    console.log(Math.round(4.7)); //5
    console.log(Math.round(4.4)); //4
    console.log(Math.ceil(4.2)); //5
    console.log(Math.floor(4.7)); //4

    //Funções Número Aleatório (Math.random)
    console.log(Math.random()); //gerar um n° aleatório entre intervalo (0 e 1)
    console.log(Math.random()*1000); //gerar número aleatório entre 0 e 1000

    //Funções Número Absoluto (Math.abs)
    console.log(Math.abs(-10)); //converte n° negativo em positivo = 10

    //Funções Analíticas Mínimo e Máximo (Math.min, Math.max)
    console.log(Math.min(1,2,3,4,5,6,7,8,9)); //1
    console.log(Math.max(1,2,3,4,5,6,7,8,9)); //9
